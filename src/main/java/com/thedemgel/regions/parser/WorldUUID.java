package com.thedemgel.regions.parser;

import java.util.UUID;
import org.spout.api.geo.World;

public class WorldUUID {

	private World world;
	private UUID uuid;

	public World getWorld() {
		return world;
	}

	public void setWorld(World world) {
		this.world = world;
	}

	public UUID getUUID() {
		return uuid;
	}

	public void setUUID(UUID uuid) {
		this.uuid = uuid;
	}
}
